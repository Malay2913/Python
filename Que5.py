my_list = [1, 2, 3]
print("Initial List:", my_list)

my_list.append(4)
my_list.append(5)
print("List after appending:", my_list)

my_list.remove(2)
print("List after removing 2:", my_list)

my_list.pop()
print("List after popping last element:", my_list)

my_list.clear()
print("List after clearing:", my_list)